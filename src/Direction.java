/**
 * Typ wyliczeniowy uzywany do wskazania kierunku w jakim ladunek ma zostac
 * przeslany przez sluzy stacji.
 */
public enum Direction {
    /**
     * Z zewnatrz do srodka stacji.
     */
    INSIDE,
    /**
     * Z srodka stacji na zewnatrz.
     */
    OUTSIDE;
}
